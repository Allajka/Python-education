def new_sum(a: float, b: float) -> float:  # задокументировали какие значения принимает функция
    return a + b


def new_diff(a: float, b: float) -> float:
    return a - b


def multiplication(a: float, b: float) -> float:
    return a * b


def division(a: float, b: float) -> float:
    return a / b if b != 0 else None
