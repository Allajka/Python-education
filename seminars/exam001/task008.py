# Сообщить в какой четверти координатной плоскости или на какой оси находится точка с координатами Х и У

x = int(input("Введите x: "))
y = int(input("Введите y: "))

if x > 0 and y > 0:
    print("Точка принадлежит четверти 1")

if x < 0 and y > 0:
    print("Точка принадлежит четверти 2")

if x < 0 and y < 0:
    print("Точка принадлежит четверти 3")
    
if x > 0 and y < 0:
    print("Точка принадлежит четверти 4")
