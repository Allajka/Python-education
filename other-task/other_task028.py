# решение математикой
# логика:  динамический проход от конца к началу с нахождением положения
#
# 1. начинаем с конца - остается один персонаж - его позиция 0 (как позиция в массиве)
#
#     позиция 1 нельзя т.к. используем остаток от деления.
#
#  res - это позиция героя.
#
# 2. делаем шаг к началу (как шаг назад во времени) - персонажей уже двое, позиция нашего героя  (res + k) % i   где i  количество персонажей (здесь двое : res = (res + k) % 2)
#
# 3. следующий шаг назад во времени - персонажей трое... позиция героя : res = (res + k) % 3...
#
# 4. шагов n - 1 (один должен остаться) , используем  range(1, n+1) или точнее  range(2, n+1) т.к. начинаем с двух персонажей (но здесь единица ничего не меняет)
#
# 5. последний шаг - начальная позиция героя со сдвигом + 1 (т.к. используем остаток от деления или положение в массиве)
n = int(input())
k = int(input())

res = 0
for i in range(1, n + 1):
    res = (res + k) % i
print(res + 1)


