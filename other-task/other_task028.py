# решение через лист
def kill(list_people, step):
    if len(list_people) == 1:
        return list_people
    elif step > len(list_people):
        index = step - (len(list_people) * (step // len(list_people)))
        del list_people[index-1]
        if not index == 1:
            if index-1 < 0:
                people.append(people.pop(index - 1))
            else:
                people.extend(people[:index-1])
                del people[:index-1]
        return kill(list_people, step)
    else:
        del people[step - 1]
        people.extend(people[:step - 1])
        del people[:step - 1]
        return kill(list_people, step)


